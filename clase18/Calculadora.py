class Calculadora:
    def sumar(a, b):
        return a + b

    def restar(a, b):
        return a - b

    def multiplicar(a, b):
        return a * b

    def dividir(a, b):
        return a / b

    def cadena_texto(texto):
        if(texto.isupper()):
            return True
        else:
            return False